"""Stan reward prompt pool for TRL GRPO training."""

from __future__ import annotations

_SYSTEM_PROMPT_STAN = (
    "/no_think\n"
    "Write a Stan model for Bernoulli coin-flip data.\n"
    "Output ONLY one ```stan code block and no prose.\n\n"
    "Rules:\n"
    "- Use this exact data interface:\n"
    "  data { int<lower=1> N; array[N] int<lower=0, upper=1> y; }\n"
    "- Include `parameters` and `model` blocks.\n"
    "- Keep the model self-contained and syntactically valid Stan.\n"
    "- Do not include explanations outside the code block.\n"
)

_STAN_PROMPTS = [
    "Model coin flips with prior beliefs about bias.",
    "Infer the coin probability from observed flips.",
    "Estimate coin bias from observed flips.",
    "Fit a Bayesian Bernoulli model for 0/1 outcomes.",
    "Write a probabilistic model for binary coin-toss observations.",
    "Infer uncertainty over a latent coin-bias parameter.",
    "Create a Stan model for Bernoulli data with explicit priors.",
    "Model binary outcomes generated by repeated coin flips.",
    "Fit a coin-toss model and estimate posterior uncertainty.",
    "Construct a Bayesian coin-bias model from 0/1 data.",
]


def get_stan_prompts(n: int) -> list[str]:
    if n <= 0:
        return []
    return [_STAN_PROMPTS[i % len(_STAN_PROMPTS)] for i in range(n)]


def load_stan_reward_prompts(
    *,
    max_examples: int | None = None,
) -> list[dict[str, list[dict[str, str]]]]:
    prompts = get_stan_prompts(max_examples or 20)
    return [
        {
            "prompt": [
                {"role": "system", "content": _SYSTEM_PROMPT_STAN},
                {"role": "user", "content": p},
            ]
        }
        for p in prompts
    ]
